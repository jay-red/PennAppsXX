var TS_1 = new Image();
TS_1.src = "src/ts1.png";

function renderGrid() {
	var tile = 0;
	for( var y = 0; y < GRIDHEIGHT; y++ ) {
		for( var x = 0; x < GRIDWIDTH; x++ ) {
			tile = GRID[ y ][ x ];
			if( tile != 0 ) {
				gameCtx.drawImage( TS_1, TILESIZE * 1, TILESIZE * 1, TILESIZE, TILESIZE, x * TILESIZE, y * TILESIZE, TILESIZE, TILESIZE );
			}
		}
	}
}

function gameLoop( ticks ) {
	if( last == -1 ) {
		last = ticks;
	}
	console.log( ticks - last );
	last = ticks;
	window.requestAnimationFrame( gameLoop );
	gameCtx.clearRect(0, 0, GAMEWIDTH, GAMEHEIGHT);
	renderGrid();
}

TS_1.addEventListener( "load", function() {
	gameLoop( 0 );
});

function pressHandler( code, pressed ) {
	switch( code ) {
		case 87:
			PRESSED.w = pressed
			break;
		case 83:
			PRESSED.s = pressed;
			break;
		case 65:
			PRESSED.a = pressed;
			break;
		case 68:
			PRESSED.d = pressed;
			break;
		case 38:
			PRESSED.up = pressed;
			break;
		case 40:
			PRESSED.down = pressed;
			break;
		case 37:
			PRESSED.left = pressed;
			break;
		case 39:
			PRESSED.right = pressed;
			break;
		case 32:
			PRESSED.space = pressed;
			break;
		default:
			break;
	}
}

function keyDownHandler( e ) {
	pressHandler( e.keyCode, true );
}

function keyUpHandler( e ) {
	pressHandler( e.keyCode, false );
}

document.addEventListener( "keydown", keyDownHandler, false );
document.addEventListener( "keyup", keyUpHandler, false );